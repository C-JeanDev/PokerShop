package model.bean;

public class BeanCategoria {

    private int    id;
    private String nome;

    public BeanCategoria() {}

    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
}