package model.bean;

public class BeanCarrello {

    private int    id;
    private String utenteEmail;

    public BeanCarrello() {}

    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }

    public String getUtenteEmail() {
        return utenteEmail;
    }
    public void setUtenteEmail(String utenteEmail) {
        this.utenteEmail = utenteEmail;
    }
}